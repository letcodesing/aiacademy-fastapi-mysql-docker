class Calculator(object):
    def __init__(self,firt,sec):
        self.firt = firt
        self.sec = sec

    def sum(self):
        return self.firt + self.sec

    def sub(self):
        return self.firt - self.sec

    def multiple(self):
        return self.firt * self.sec
    
    def devide(self):
        return self.firt / self.sec
        
